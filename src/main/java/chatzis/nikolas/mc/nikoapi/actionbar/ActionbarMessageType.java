package chatzis.nikolas.mc.nikoapi.actionbar;

/**
 * Enum which defines the type of action bar message.
 * A IMPORTANT actionbar message, will be called immediately. runAutomatically ticks will be ignored.
 * @author HeyImDome
 * @since 0.0.1
 */
public enum ActionbarMessageType {

    STATIC,
    TIMED,
    IMPORTANT

}
